// Operatori Matematici

let x;
let y;

x = 15;
y = 2.5;

const PI = 3.14;

// adunare
console.log("Adunare: " + x + y);

// scadere
console.log("Scadere: " + (x - y));

// inmultire
console.log("Inmultire: " + x * y);

// impartire
console.log("Impartire: " + x / y);

// impartire cu ramas: modulus
console.log("Impartire (modulus): " + x % y);

// valoarea PI
console.log("Valoarea PI: " + PI * 2);


